// Generate web/config.js from WF_* env (and .env if present) for no-docker `dev:all`.
// Mirrors web/docker-entrypoint.sh. Safe to run repeatedly.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join, dirname } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
// load .env (simple KEY=VALUE parser) so local dev can keep URLs in one place
const envPath = join(root, '.env');
if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, 'utf8').split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (m && process.env[m[1]] === undefined) process.env[m[1]] = m[2].replace(/^["']|["']$/g, '');
  }
}

const cfg = {
  sponsorUrl: process.env.WF_SPONSOR_URL || 'http://localhost:8788/sponsor',
  portalUrl: process.env.WF_PORTAL_URL || 'http://localhost:8790',
  llmProxyUrl: process.env.WF_LLM_PROXY_URL || 'http://localhost:8787/llm',
  zkGoogleClientId: process.env.WF_ZK_GOOGLE_CLIENT_ID || '',
  zkSaltUrl: process.env.WF_ZK_SALT_URL || 'http://localhost:8789/salt',
  zkProverUrl: process.env.WF_ZK_PROVER_URL || '',
  importProxyUrl: process.env.WF_IMPORT_URL || '',
};
writeFileSync(join(root, 'web', 'config.js'),
  `// generated by scripts/gen-web-config.mjs\nwindow.__WF_CONFIG = ${JSON.stringify(cfg, null, 2)};\n`);
console.log('wrote web/config.js', cfg);
